    @Override
    public void setCurrentData(Record record) {
        if (!(record instanceof Experiment)) {
            return;
        }
        
        Experiment experiment = (Experiment) record;
        if (experiment == null) {
            this.dispName.setText("");
            this.dispDescription.setText("");
            
        } else {
            this.dispName.setText(experiment.getName());
            this.dispDescription.setText(experiment.getDescription());
        }
    }

