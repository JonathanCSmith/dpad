    @Override
    public Record buildNewlySubmittedRecord() {
        String n = this.name.getText();
        String d = this.description.getText();

        if (!n.contentEquals("") && !d.contentEquals("")) {
            Experiment experiment = new Experiment();
            experiment.setName(n);
            experiment.setDescription(d);
            
            return experiment;
        } 

        else {
            return null;
        }
    }

    @Override
    public void clearInfo() {
        this.name.setText("");
        this.description.setText("");
    }

